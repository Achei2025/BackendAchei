package br.gov.achei.achei.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
// import org.springframework.security.config.Customizer;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.crypto.bcrypt.BCryptPasswordEncoder;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.security.web.SecurityFilterChain;

@Configuration
@EnableWebSecurity
public class SecurityConfig {

    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        // Desativando a proteção CSRF para testes (não recomendado para produção)
        http.csrf(csrf -> csrf.disable())
            // Permitir todas as requisições sem autenticação para testes
            .authorizeHttpRequests(authorize -> authorize
                .anyRequest().permitAll()
            );

        // Esta configuração é temporária e deve ser ajustada para produção
        // No ambiente de produção, configure as políticas de segurança adequadas

        return http.build();
    }

    // Definindo um Bean para o PasswordEncoder
    @Bean
    public PasswordEncoder passwordEncoder() {
        return new BCryptPasswordEncoder();
    }
}
